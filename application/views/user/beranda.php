        <h1 class="text-center h1 pb-4">Tentang Sekolah</h1>
        <p>Pada 7 Agustus 1981 berdiri Program Pendidikan Ilmu Komputer (PPIK) di Jakarta yang tiga tahun kemudian berubah menjadi Sekolah Tinggi Manajemen Informatika dan Komputer (STMIK) Gunadarma. Enam tahun kemudian, tepatnya pada 13 Januari 1990, berdiri Sekolah Tinggi Ilmu Ekonomi Gunadarma (STIE Gunadarma).

            Pada tahun 1993, STMIK dan STIE membuka Program Magister dengan konsentrasi Manajemen Sistem Informasi untuk STMIK serta Magister Manajemen untuk STIE. Selanjutnya, melalui S.K. Dirjen DIKTI No.92/Kep/Dikti/1996 tertanggal 3 April 1996, STMIK dan STIE Gunadarma melebur menjadi UNIVERSITAS GUNADARMA bersama dengan empat fakultas baru yaitu Fakultas Teknologi Industri, Fakultas Teknik Sipil dan Perencanaan, Fakultas Psikologi, dan Fakultas Sastra. Membuka milenium baru, Universitas Gunadarma membuka Program Doktor Ilmu Ekonomi berdasarkan ijin Dirjen Dikti Departemen Pendidikan Republik Indonesia No. 55/DIKTI/2000 yang diikuti dengan pembukaan Program Doktor Teknologi Informasi berdasarkan ijin Dirjen Dikti Departemen Pendidikan Republik Indonesia No. 3716/P/T/2002.

            Setelah melalui perjuangan panjang dalam menghadapi berbagai tantangan, Universitas Gunadama telah menjelma menjadi salah satu perguruan tinggi swasta yang berbasis teknologi informasi dan komunikasi terkemuka di Indonesia. Pengalaman selama 25 tahun yang diiringi pengabdian dan dedikasi tenaga pengajar; komitmen yayasan dan pimpinan; pengadaan fasilitas pembelajaran serta kepercayaan masyarakat, Universitas Gunadarma terus berupaya menghasilkan lulusan yang berguna dan bisa mendarmabaktikan kompetensinya demi membangun masa depan bangsa yang lebih baik. Beberapa tonggak historis Universitas Gunadarma dalam kurun waktu seperempat abad disajikan dalam Tabel Tonggak Historis Universitas Gunadarma</p>
